% Факты, представляющие лабиринт.
% Путь между клетками (x, y) и (x, y) обозначен как connected(x1, y1, x2, y2).
connected(1, 1, 1, 2).
connected(1, 2, 1, 3).
connected(1, 3, 2, 3).
connected(2, 3, 3, 3).
connected(3, 3, 3, 2).
connected(3, 2, 3, 1).
connected(3, 1, 2, 1).
connected(2, 1, 1, 1).

% Правило: если существует путь от одной клетки до другой, то между ними можно пройти.
path(X1, Y1, X2, Y2) :- connected(X1, Y1, X2, Y2).
path(X1, Y1, X2, Y2) :-
    connected(X1, Y1, X3, Y3),
    path(X3, Y3, X2, Y2).

